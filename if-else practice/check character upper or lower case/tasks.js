                    


let character = prompt("Enter a character ...");


if (character >= 'A' && character <= 'Z') {
    document.write("Entered Character is in UpperCase..<br>");
} else if (character >= 'a' && character <= 'z') {
    document.write("Entered Character is in lowerCase..<br>");
} else {
    document.write("Not an alphabet character..<br>");
}