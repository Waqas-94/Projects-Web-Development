                    


let num = Number (prompt("Enter a number ..."));


if(num>0)
{
    document.write("Entered number "+num + " is POSITIVE <br>");
}
else if(num<0)
{
    document.write("Entered number "+num + " is NEGATIVE <br>");
}
else if(num==0)
{
    document.write("Entered number is ZERO <br>");
}
else
{
    document.write("Please Enter only Numeric Numbers...<br>");
}