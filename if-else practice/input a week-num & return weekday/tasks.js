                    


let num = Number (prompt("Enter a Week Number (1~7) ..."));


if(num==1)
{
    document.write("Its Sunday...<br>");
}
else if(num==2)
{
    document.write("Its Monday...<br>");
}
else if(num==3)
{
    document.write("Its Tuesday...<br>");
}
else if(num==4)
{
    document.write("Its Wednesday...<br>");
}
else if(num==5)
{
    document.write("Its Thursday...<br>");
}
else if(num==6)
{
    document.write("Its Friday...<br>");
}
else if(num==7)
{
    document.write("Its Saturday...<br>");
}
else
{
    document.write("Entered number between given range only...<br>");
}