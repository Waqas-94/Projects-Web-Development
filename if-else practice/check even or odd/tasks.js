                    


let num = Number (prompt("Enter a number ..."));


if(num%2==0)
{
    document.write("Entered number "+num + " is EVEN..<br>");
}
else if((num-1)%2==0)
{
    document.write("Entered number "+num + " is ODD..<br>");
}

else
{
    document.write("ENTER numeric number only...<br>");
}